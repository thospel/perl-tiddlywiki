         ## no critic (UselessNoCritic MagicNumbers)
         ExtUtils::MakeMaker->VERSION(6.55_01);
         1;
     } ? (BUILD_REQUIRES	=> {
         # Only for the tests
         "Test::More"	=> "0.01",
     }) : (),
     PREREQ_PM		=> {
         # Arbitrarily picked version. Released in 2014 which should be OK
         "XML::Parser"	=> "2.43",
     },
     ABSTRACT		=> 'Mananage tiddlywiki classic in git',
     AUTHOR		=> 'Ton Hospel <Wiki-TiddlyWiki-Classic-Saver@ton.iguana.be>',
     PL_FILES		=> {},
     EXE_FILES		=> \@programs_to_install,
     $^O eq "MSWin32" ? (
         PM_FILTER	=> '$(PERL) -p -e1',
     ) : (),
     clean		=> {
         FILES => '$(DISTNAME).ppd ppm cover_db nytprof nytprof.out* t/options t/options.* md5-versions.old',
     },
     #Value must be from legacy list of licenses here
     #http://search.cpan.org/perldoc?Module%3A%3ABuild%3A%3AAPI
     LICENSE		=> 'perl',
 );

